package main

import "fmt"

func main() {
	// Integers
	var integer int = 5
	fmt.Println("Integer: ", integer)

	// Float
	var floatNumber float64 = 5.5
	fmt.Println("Float number: ", floatNumber)

	// String
	var name string = "Golang"
	fmt.Println("Name: ", name)

	// Boolean
	var isItBool bool = true
	fmt.Println("IsItBoolean: ", isItBool)
}
